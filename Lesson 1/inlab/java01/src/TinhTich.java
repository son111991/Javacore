public class TinhTich {
  public static void main(String[] args) {
    int x = 50;
    int y = 7;
    int tich = x * y;
    float thuong = (float) x / y;
    System.out.println("Tích của 2 số " + x + " và " + y + " là "+ tich);
    System.out.println("Thương của 2 số " + x + " và " + y + " là "+ thuong);
  }
}
